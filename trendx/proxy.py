def make_proxy_client(user: str, password: str) -> httpx.AsyncClient:
    """Create an httpx client routed through IPRoyal residential proxy."""
    proxy_url = f"http://{user}:{password}@geo.iproyal.com:12321"
    return httpx.AsyncClient(proxy=proxy_url, verify=True, timeout=30.0)


def make_direct_client() -> httpx.AsyncClient:
    """Create an httpx client for direct (non-proxied) requests."""
    return httpx.AsyncClient(verify=True, timeout=30.0)


async def fetch(url: str, client: httpx.AsyncClient, accept: str = "application/json") -> httpx.Response:
    """Fetch URL through proxy with random UA and human-like delay."""
    delay = random.uniform(2.0, 5.0)
    await asyncio.sleep(delay)
    return await client.get(url, headers={
        "User-Agent": random.choice(USER_AGENTS),
        "Accept": accept,
        "Accept-Language": "en-US,en;q=0.9",
    })


async def fetch_direct(url: str, client: httpx.AsyncClient, accept: str = "application/json") -> httpx.Response:
    """Fetch URL directly (no delay for APIs that allow it)."""
    delay = random.uniform(0.5, 1.5)
    await asyncio.sleep(delay)
    return await client.get(url, headers={
        "User-Agent": random.choice(USER_AGENTS),
        "Accept": accept,
        "Accept-Language": "en-US,en;q=0.9",
    })
